


window.onload = function() {
    const nodo = document.getElementById("Caja");
    const fragment = document.createDocumentFragment();

    for (let i = 0; i < 10000; i++) {
        const nodoP = document.createElement("script");
        const texto = document.createTextNode(`console.log('Hola ${i}');`);
        nodoP.appendChild(texto);
        fragment.appendChild(nodoP);
    }
    nodo.append(fragment);

};
